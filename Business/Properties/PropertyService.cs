            }
            return m;
        }

        /// <summary>
        /// The DisableProperty.
        /// </summary>
        /// <param name="parameters">The parameters<see cref="Dictionary{string, dynamic}"/>.</param>
        /// <returns>The <see cref="Task{ResponseModel}"/>.</returns>
        public async Task<ResponseModel> DisableProperty(Dictionary<string, dynamic> parameters)
        {
            try
            {
                var res = await dao.DisableProperty<PropertyPutModel>(parameters);
