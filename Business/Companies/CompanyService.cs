            }
            return m;
        }

        /// <summary>
        /// The DisableCompany.
        /// </summary>
        /// <param name="parameters">The parameters<see cref="Dictionary{string, dynamic}"/>.</param>
        /// <returns>The <see cref="Task{ResponseModel}"/>.</returns>
        public async Task<ResponseModel> DisableCompany(Dictionary<string, dynamic> parameters)
        {
            try
