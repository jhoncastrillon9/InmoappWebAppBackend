namespace Business.Owners
{
    using Microsoft.Extensions.Configuration;
    using System;
    using System.Collections.Generic;
    using System.Threading.Tasks;

    using CodeMono.Entities;
    using Entities.Owners;
    using DataAccess.Owners;
    
    /// <summary>
    /// Defines the <see cref="OwnerService" />.
    /// </summary>
    public class OwnerService
    {
        /// <summary>
        /// Defines the dao.
        /// </summary>
        private readonly OwnerDao dao;

        /// <summary>
        /// Defines the m.
        /// </summary>
        private ResponseModel m;

        /// <summary>
        /// Initializes a new instance of the <see cref="OwnerService"/> class.
        /// </summary>
        /// <param name="config">The config<see cref="IConfiguration"/>.</param>
        /// <param name="con">The con<see cref="string"/>.</param>
        public OwnerService(IConfiguration config, string con)
        {
            dao = new OwnerDao(config, con);
            m = new ResponseModel();
        }

        /// <summary>
        /// The GetOwner.
        /// </summary>
        /// <param name="parameters">The parameters<see cref="Dictionary{string, dynamic}"/>.</param>
        /// <returns>The <see cref="Task{ResponseModel}"/>.</returns>
        public async Task<ResponseModel> GetOwner(Dictionary<string, dynamic> parameters)
        {
            try
            {
                var res = await dao.GetOwner<OwnerModel>(parameters);
                m.data = res;
                m.executionError = false;
                m.message = "";
            }
            catch (Exception ex)
            {
                m.data = null;
                m.executionError = true;
                m.message = "Error: " + ex.Message + ". " + ex.InnerException;
            }
            return m;
        }

        /// <summary>
